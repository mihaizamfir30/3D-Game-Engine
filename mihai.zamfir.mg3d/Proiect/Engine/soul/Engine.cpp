bool Engine::update() {

	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	myWindow->OnFrameStart();

	bool ret = myWindow->update();


	scene.OnFrameStart();
	go.Draw();
	scene.OnFrameEnd();

	if (ret == true) {
		myWindow->OnFrameEnd();
	}

	return ret;
}
